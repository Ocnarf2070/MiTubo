CREATE OR REPLACE PROCEDURE MODIFICA 
(
 NOMBRE_NUEVO IN VARCHAR2,
 AP1 IN VARCHAR2,
 AP2 IN VARCHAR2,
 DES IN VARCHAR2,
 CORREO IN VARCHAR2
) AS 
BEGIN
  UPDATE USUARIO SET NOMBRE = NOMBRE_NUEVO,
  APELLIDO1=AP1,
  APELLIDO2=AP2,
  DESCRIPCION=DES,
  EMAIL=CORREO 
  WHERE ALIAS=USER;
  
END MODIFICA;